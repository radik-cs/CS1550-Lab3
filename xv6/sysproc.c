int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// implementing the syscall handler sys_setpriority. In this function, we need to check that the new priority is valid (in the range of [0, 200]), 
// update the processâ€™s priority, and, if the new priority is larger than the old priority, call yield to reschedule. 

int sys_setpriority(void) {
  struct proc *proc = myproc();
  int new_prior;
  int old_prior;

  argint(0, &new_prior);
  old_prior = proc->priority;
  proc->priority = new_prior;
  if (0 <= new_prior && new_prior <= 200) {
    if (new_prior > old_prior)
    {
      yield();
    }
    return 0;
  }
  return -1;
}